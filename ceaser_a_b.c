#include <stdio.h>
#include <string.h>

char decryptChar(int c, int a, int b) 
{
    return ((a * (c - 'A' - b) % 26) + 26) % 26 + 'A';
}

int main() 
{
    char ciphertext[1000];
    printf("Enter the ciphertext: ");
    scanf("%s", ciphertext);

    printf("Possible plaintexts using affine Caesar cipher:\n");

    for (int a = 1; a < 26; a++) 
    {
        for (int b = 0; b < 26; b++) 
        {
            printf("a = %d, b = %d: ", a, b);
            for (int i = 0; i < strlen(ciphertext); i++) 
            {
                printf("%c", decryptChar(ciphertext[i], a, b));
            }
            printf("\n");
        }
    }

    return 0;
}


